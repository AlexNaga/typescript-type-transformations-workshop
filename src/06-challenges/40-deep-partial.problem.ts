import { Equal, Expect } from "../helpers/type-utils";

type DeepPartial<T> = unknown;

type MyType = {
  a: string;
  b: number;
  c: {
    d: string;
    e: {
      f: string;
      g: {
        h: string;
        i: string;
      }[];
    };
  };
};

type tests = [
  Expect<
    Equal<
      DeepPartial<MyType>,
      {
        a?: string;
        b?: number;
        c?: {
          d?: string;
          e?: {
            f?: string;
            g?: {
              h?: string;
              i?: string;
            }[];
          };
        };
      }
    >
  >,
];
